static inline uint32_t rotl8(uint32_t x) { return (x << 8) | (x >> 24); }

static inline uint32_t subword(uint32_t w)
{
	return ((uint32_t)sbox[(w >> 24) & 0xffu] << 24) |
	       ((uint32_t)sbox[(w >> 16) & 0xffu] << 16) |
	       ((uint32_t)sbox[(w >> 8) & 0xffu] << 8) |
	       ((uint32_t)sbox[w & 0xffu]);
}

static inline uint32_t load_be32(const uint8_t *p)
{
	return ((uint32_t)p[0] << 24) | ((uint32_t)p[1] << 16) | ((uint32_t)p[2] << 8) | (uint32_t)p[3];
}

static inline void store_be32(uint8_t *p, uint32_t v)
{
	p[0] = (uint8_t)(v >> 24);
	p[1] = (uint8_t)(v >> 16);
	p[2] = (uint8_t)(v >> 8);
	p[3] = (uint8_t)v;
}

static inline uint8_t xtime(uint8_t x)
{
	return (uint8_t)((x << 1) ^ ((x & 0x80u) ? 0x1bu : 0x00u));
}

static inline void sub_bytes(uint8_t s[16])
{
	for (int i = 0; i < 16; i++) s[i] = sbox[s[i]];
}

static inline void shift_rows(uint8_t s[16])
{
	uint8_t t;
	/* row 1 */
	t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
	/* row 2 */
	t = s[2]; s[2] = s[10]; s[10] = t;
	t = s[6]; s[6] = s[14]; s[14] = t;
	/* row 3 */
	t = s[3]; s[3] = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = t;
}

static inline void mix_columns(uint8_t s[16])
{
	for (int c = 0; c < 4; c++) {
		uint8_t *p = &s[c * 4];
		uint8_t a0 = p[0], a1 = p[1], a2 = p[2], a3 = p[3];
		uint8_t t = (uint8_t)(a0 ^ a1 ^ a2 ^ a3);
		uint8_t u = a0;
		p[0] ^= t ^ xtime((uint8_t)(a0 ^ a1));
		p[1] ^= t ^ xtime((uint8_t)(a1 ^ a2));
		p[2] ^= t ^ xtime((uint8_t)(a2 ^ a3));
		p[3] ^= t ^ xtime((uint8_t)(a3 ^ u));
	}
}

static inline void add_round_key(uint8_t s[16], const uint32_t *rk)
{
	for (int i = 0; i < 4; i++) {
		uint32_t w = rk[i];
		s[i * 4 + 0] ^= (uint8_t)(w >> 24);
		s[i * 4 + 1] ^= (uint8_t)(w >> 16);
		s[i * 4 + 2] ^= (uint8_t)(w >> 8);
		s[i * 4 + 3] ^= (uint8_t)(w);
	}
}

void aes128_init(struct aes128_ctx *ctx, const uint8_t key[AES128_KEY_SIZE])
{
	static const uint32_t rcon[10] = {
		0x01000000u,0x02000000u,0x04000000u,0x08000000u,0x10000000u,
		0x20000000u,0x40000000u,0x80000000u,0x1b000000u,0x36000000u,
	};

	ctx->rk[0] = load_be32(&key[0]);
	ctx->rk[1] = load_be32(&key[4]);
	ctx->rk[2] = load_be32(&key[8]);
	ctx->rk[3] = load_be32(&key[12]);

	for (uint32_t i = 4; i < 44; i++) {
		uint32_t temp = ctx->rk[i - 1];
		if ((i & 3u) == 0) {
			temp = subword(rotl8(temp)) ^ rcon[(i / 4u) - 1u];
		}
		ctx->rk[i] = ctx->rk[i - 4] ^ temp;
	}
}

void aes128_encrypt_block(const struct aes128_ctx *ctx, const uint8_t in[AES_BLOCK_SIZE], uint8_t out[AES_BLOCK_SIZE])
{
	uint8_t s[16];
	for (int i = 0; i < 16; i++) s[i] = in[i];

	add_round_key(s, &ctx->rk[0]);
	for (int round = 1; round <= 9; round++) {
		sub_bytes(s);
		shift_rows(s);
		mix_columns(s);
		add_round_key(s, &ctx->rk[round * 4]);
	}
	/* final */
	sub_bytes(s);
	shift_rows(s);
	add_round_key(s, &ctx->rk[40]);

	for (int i = 0; i < 16; i++) out[i] = s[i];
	crypto_memset(s, 0, sizeof(s));
}
